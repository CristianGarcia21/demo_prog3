def divicion(x,y):
    return x/y

def resta(x,y):
    return x-y

def suma(x,y):
    return x+y

respuesta = resta(6,3)
print("resultado es: " + str(respuesta))
respuesta = divicion(6,3)
print("resultado es: " + str(respuesta))
resultadoSuma = suma(8,9)
