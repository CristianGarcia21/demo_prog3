def divicion(x,y):
    return x/y

def resta(x,y):
    return x-y

def suma(x,y):
    """
    Documentacion de la funcion suma

    Esta funcion recibe dos parametros y devuelve la suma de ambos

    Parametros:
    x -- primer numero
    y -- segundo numero
    
    Retorno:
    x+y
    """
    return x*y

respuesta = resta(6,3)
